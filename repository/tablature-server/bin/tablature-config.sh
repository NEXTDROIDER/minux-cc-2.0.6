print("are these settings correct?")
print("hit enter to confirm, ctrl+t to abort")
print("------------------------------")
print("webroot:/"..webroot)
print("hostname:"..hostname)
temp = read()
local id = tonumber(os.getComputerID())
print("lID:"..id)
if _G.server == "[none]" then
    print("skipping register")
else
    print("attempting to register on dhcp")
    dhcp.register(hostname)
    print("testing lookup")
    dhcp.lookup(hostname)
    local rid = tonumber(_G.lookupid)
    print("rID:"..rid)
    if rid ~= id then
        print("register failed, only direct id will work")
        read()
    end
end
print("storing configuration data")
local tempfile = fs.open("/usr/tablature-server/settings.cfg","w")
tempfile.writeLine(webroot)
tempfile.writeLine(id)
tempfile.close()
local runfile = fs.open("/usr/tablature/first.run","w")
runfile.writeLine("done")
runfile.close()
minux.download("https://minux.cc/apt/tb/index.html",webroot.."/index.html")
