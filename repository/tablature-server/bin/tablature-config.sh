term.clear()
term.setCursorPos(1,1)
print("Tablature configuration wizard")
print("------------------------------")
print("next we need to register on the dhcp server")
print("we will need to have a name to register under")
print("this makes it possible to browse to this name")
print("or to find it on the network with the dhcp tools")
print("what name does this system have?")
print("------------------------------")
